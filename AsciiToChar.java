public class AsciiToChar {

    public static void main(String[] args) {
        int asciiValue1 = 65; 
        char character1 = (char) asciiValue1;
        System.out.println("The character for ASCII value " + asciiValue1 + " is: " + character1);
    
        int asciiValue2 = 97; 
        char character2 = (char) asciiValue2;
        System.out.println("The character for ASCII value " + asciiValue2 + " is: " + character2);
   }
}